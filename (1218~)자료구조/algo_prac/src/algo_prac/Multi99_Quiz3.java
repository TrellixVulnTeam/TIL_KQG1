public class Multi99_Quiz3 {

	public static void main(String[] args) {
		//다중 루프 : 반복이 중첩되는 것
		
		for(int i=1;i<=5;i++) {
			
			for(int j=1; j<5; j++) {
				System.out.print("*");
			}
			System.out.println("*");
		}
		

	}

}

